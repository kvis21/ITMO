/*
Вывести список студентов, имеющих одинаковые отчества, но не совпадающие даты рождения.
*/
select Н_ЛЮДИ.ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ, Н_ЛЮДИ.ОТЧЕСТВО, Н_ЛЮДИ.ДАТА_РОЖДЕНИЯ 
from Н_ЛЮДИ 
where Н_ЛЮДИ.ОТЧЕСТВО in (
        select ОТЧЕСТВО
        from Н_ЛЮДИ inner join Н_УЧЕНИКИ on Н_ЛЮДИ.ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
        group by ОТЧЕСТВО
        having count(distinct ДАТА_РОЖДЕНИЯ) > 1
    )
order by Н_ЛЮДИ.ОТЧЕСТВО, Н_ЛЮДИ.ДАТА_РОЖДЕНИЯ;






