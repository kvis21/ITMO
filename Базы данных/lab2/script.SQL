/* 
Сделать запрос для получения атрибутов из указанных таблиц, применив фильтры по указанным условиям:
Таблицы: Н_ОЦЕНКИ, Н_ВЕДОМОСТИ.
Вывести атрибуты: Н_ОЦЕНКИ.ПРИМЕЧАНИЕ, Н_ВЕДОМОСТИ.ЧЛВК_ИД.
Фильтры (AND):
a) Н_ОЦЕНКИ.ПРИМЕЧАНИЕ < неудовлетворительно.
b) Н_ВЕДОМОСТИ.ДАТА > 1998-01-05.
Вид соединения: INNER JOIN.
*/

select Н_ОЦЕНКИ.ПРИМЕЧАНИЕ, Н_ВЕДОМОСТИ.ЧЛВК_ИД, Н_ВЕДОМОСТИ.ДАТА, Н_ОЦЕНКИ.ПРИМЕЧАНИЕ
from Н_ВЕДОМОСТИ
inner join Н_ОЦЕНКИ
on Н_ВЕДОМОСТИ.ОЦЕНКА = Н_ОЦЕНКИ.КОД
where Н_ОЦЕНКИ.ПРИМЕЧАНИЕ < 'неудовлетворительно'
and Н_ВЕДОМОСТИ.ДАТА > '1998-01-05';


-- №2
/*
Сделать запрос для получения атрибутов из указанных таблиц, применив фильтры по указанным условиям:
Таблицы: Н_ЛЮДИ, Н_ОБУЧЕНИЯ, Н_УЧЕНИКИ.
Вывести атрибуты: Н_ЛЮДИ.ОТЧЕСТВО, Н_ОБУЧЕНИЯ.НЗК, Н_УЧЕНИКИ.ГРУППА.
Фильтры: (AND)
a) Н_ЛЮДИ.ОТЧЕСТВО < Александрович.
b) Н_ОБУЧЕНИЯ.ЧЛВК_ИД < 163276.
c) Н_УЧЕНИКИ.ГРУППА = 1101.
Вид соединения: INNER JOIN.
*/

select Н_ЛЮДИ.ОТЧЕСТВО, Н_ОБУЧЕНИЯ.НЗК, Н_УЧЕНИКИ.ГРУППА
from Н_ЛЮДИ
inner join Н_ОБУЧЕНИЯ
	on Н_ЛЮДИ.ИД = Н_ОБУЧЕНИЯ.ЧЛВК_ИД
inner join Н_УЧЕНИКИ
	on Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ОБУЧЕНИЯ.ЧЛВК_ИД
where Н_ЛЮДИ.ОТЧЕСТВО < 'Александрович'
	and Н_ОБУЧЕНИЯ.ЧЛВК_ИД < 163276
	and Н_УЧЕНИКИ.ГРУППА = '1101'; 

