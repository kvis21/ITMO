--№4
/*
В таблице Н_ГРУППЫ_ПЛАНОВ найти номера планов, по которым обучается (обучалось) менее 2 групп на заочной форме обучения.
Для реализации использовать подзапрос.
 */


select Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД from (
	select Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД
	from Н_ГРУППЫ_ПЛАНОВ 
	join Н_ПЛАНЫ using(ПЛАН_ИД)
	join Н_ФОРМЫ_ОБУЧЕНИЯ on Н_ПЛАНЫ.ФО_ИД=Н_ФОРМЫ_ОБУЧЕНИЯ.ИД
	where Н_ФОРМЫ_ОБУЧЕНИЯ.НАИМЕНОВАНИЕ = 'Очная'
	group by ПЛАН_ИД
	having COUNT(ГРУППА)<2
);

--№5
/*
Выведите таблицу со средними оценками студентов группы 4100 (Номер, ФИО, Ср_оценка),
у которых средняя оценка меньше минимальной оценк(е|и) в группе 3100.
 */

create temp table if not exists marks as
select Н_УЧЕНИКИ.*,
    cast(
	case ОЦЕНКА
		when '2' then '2'	
		when '3' then '3'	
		when '4' then '4'
		when '5' then '5'
		when '99' then '5'
		when 'зачет' then '5'
		when 'незач' then '2'
		when 'неявка' then '2'
		when 'осв' then '2'
		end as INTEGER
    ) as mark
from Н_ВЕДОМОСТИ
join Н_УЧЕНИКИ on Н_ВЕДОМОСТИ.ЧЛВК_ИД = Н_УЧЕНИКИ.ЧЛВК_ИД;

select Н_УЧЕНИКИ.ЧЛВК_ИД, Н_ЛЮДИ.ФАМИЛИЯ,  Н_ЛЮДИ.ИМЯ, Н_ЛЮДИ.ОТЧЕСТВО, avg_mark from Н_УЧЕНИКИ
join Н_ЛЮДИ on Н_ЛЮДИ.ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
join (select ЧЛВК_ИД, 
    avg(mark) as avg_mark
	from marks
	group by ЧЛВК_ИД
) using(ЧЛВК_ИД)
where ГРУППА in ('3100', '4100')
and (
	select min(mark) as min_group_mark
	from marks
	where ГРУППА = '3100'
	group by ГРУППА 
) > avg_mark;

--№6
/*
Получить список студентов, зачисленных после первого сентября 2012 года на первый курс очной или заочной формы обучения (специальность: Программная инженерия). В результат включить:
номер группы;
номер, фамилию, имя и отчество студента;
номер и состояние пункта приказа;
Для реализации использовать подзапрос с IN.
 */

SELECT Н_УЧЕНИКИ.ГРУППА, Н_УЧЕНИКИ.ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ, Н_ЛЮДИ.ОТЧЕСТВО, Н_УЧЕНИКИ.П_ПРКОК_ИД, Н_УЧЕНИКИ.СОСТОЯНИЕ
from Н_УЧЕНИКИ
join Н_ЛЮДИ on Н_ЛЮДИ.ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
join Н_ПЛАНЫ on Н_УЧЕНИКИ.ПЛАН_ИД = Н_ПЛАНЫ.ИД
join Н_ФОРМЫ_ОБУЧЕНИЯ on Н_ПЛАНЫ.ФО_ИД = Н_ФОРМЫ_ОБУЧЕНИЯ.ИД
    and (Н_ФОРМЫ_ОБУЧЕНИЯ.НАИМЕНОВАНИЕ = 'Заочная'
    or Н_ФОРМЫ_ОБУЧЕНИЯ.НАИМЕНОВАНИЕ = 'Очная')
join Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ on Н_ПЛАНЫ.НАПС_ИД = Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ.ИД
join Н_НАПР_СПЕЦ on Н_НАПР_СПЕЦ.ИД = Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ.НС_ИД
    and Н_НАПР_СПЕЦ.НАИМЕНОВАНИЕ = 'Программная инженерия'
where Н_ПЛАНЫ.КУРС = 1 and Н_УЧЕНИКИ.ИД in (
	select Н_УЧЕНИКИ.ИД from Н_УЧЕНИКИ
	where Н_УЧЕНИКИ.НАЧАЛО > '2012-09-01'
);

